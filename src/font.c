static int getCharIndex(char c) {
    c = toupper(c);
    
    if(c >= 'A' && c <= 'Z') {
        return c - 'A';  // 0-25
    } else if(c >= '0' && c <= '9') {
        return 26 + (c - '0');  // 26-35
    } else if(c == '!') {
        return 36;
    } else if(c == '.') {
        return 37;
    } else if(c == ':') {
        return 38;
    } else if(c == '-') {
        return 39;
    } else if(c == ' ') {
        return 40;
    }
    
    return 40;  // Space als Fallback
}

void drawText(const char* text, float x, float y, u32 color, float scale) {
    float cursorX = x;
    int len = strlen(text);
    
    for(int i = 0; i < len; i++) {
        int charIndex = getCharIndex(text[i]);
        
        // Zeichne Buchstabe Pixel für Pixel
        for(int row = 0; row < 7; row++) {
            unsigned char rowData = font_data[charIndex][row];
            
            for(int col = 0; col < 5; col++) {
                if(rowData & (1 << (4 - col))) {
                    // Pixel ist gesetzt
                    GRRLIB_Rectangle(
                        cursorX + col * scale,
                        y + row * scale,
                        scale,
                        scale,
                        color,
                        true
                    );
                }
            }
        }
        
        cursorX += 6 * scale;  // 5 Pixel Breite + 1 Pixel Abstand
    }
}

void drawTextCentered(const char* text, float y, u32 color, float scale) {
    int len = strlen(text);
    float textWidth = len * 6 * scale;
    float x = (SCREEN_WIDTH - textWidth) / 2.0f;
    drawText(text, x, y, color, scale);
}
